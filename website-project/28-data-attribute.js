/**
 * HTML5 introduced the data-* attribute
 * 
 * It allows us to store some extra information in our html element with no particular meaning
 * 
 * The syntax is easy
 * 
 * You just need to include an attribute with a name that starts with data-
 * 
 * Remember, we need to then use JavaScript to read the value of this attribute
 * 
 * Reading the values with JavaScript is also easy
 * 
 * We just use the . notation with the dataset object
 * for example, element.dataset.[yourDataName]
 */