/**
 * What is Events?
 * Actions taken by the user or browser
 * 
 * Mostly, you will want to run code when the user does something
 * There are many different types of events
 * e.g. When a user clicks a button, a click event is fired
 * e.g. When a user presses a key, a keypress event is fired
 * e.g. When your browser fetches and loads all data, a load event is fired
 * 
 * 
 * You need to unserstand 2 things in order to master events
 * 1. listener (inline events, inline properties, event listeners)
 * 2. handler
 * 
 * listener listens out for the event
 * handler is a code that runs when an event has fired
 */