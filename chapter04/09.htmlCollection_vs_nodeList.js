/**
 * NodeList vs HTMLCollection
 * 1. Both a NodeList and an HTMLCollection are collections of DOM nodes.
 * 2. A NodeList can contain any node type (text, comment, element, etc)
 * 3. An HTMLCollection only contains an element node.
 * 4. HTMLCollections can be accessed by their name, id or index number
 * 5. NodeList items can only be accessed by their index number.
 * 6. HTMLCollections are live, and NodeList items are typically static.
 */